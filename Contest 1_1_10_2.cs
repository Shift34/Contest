            int k = Convert.ToInt32(filename[0]);
            int p = 0;
            int count = 0;
            using (StreamWriter sw = new StreamWriter("output.txt", false))
            {
                for (int i = 0; i < k; i++)
                {
                    count = 0;
                    int s = Convert.ToInt32(filename[1 + p]);
                    string[] y = new string[2];
                    for (int w = 0; w < 2; w++)
                    {
                        y[w] = filename[2 + p + w];
                    }
                    char[] massage = new char[s];
                    for (int q = 0; q < s; q++)
                    {
                        if ((y[0][q] == '1') && (y[1][q] == '1'))
                        {
                            count = +1;
                            break;
                        }
                    }
                    if (count == 0)
                    {
                        sw.WriteLine("YES");
                    }
                    else sw.WriteLine("NO");
                    p += 3;
                }
            }
        }
    }
}
